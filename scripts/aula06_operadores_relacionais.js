/** operadores relacionais
 * | > -> maior que
 * | >= maior ou igual
 * | < -> menor que
 * | <= menor ou igual
 * | == -> igual igual
 * | === -> além de verificar se é igual ele verifica o tipo ex: string, inteiro, float e etc
 * | != -> diferente
 * | !== -> verifica se é diferente e o tipo também ex; 10 != 5 = sim | 10 !== '10' sim
 * | ! -> negação
 */

let num1 = 10
let num2 = 5
let num3 = 10

console.log(num1 > num2);
console.log(num1 >= num3);
console.log(num1 < num2);
console.log(num1 <= num3);